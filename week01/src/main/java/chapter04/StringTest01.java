package chapter04;

public class StringTest01 {
	public static void main(String[] args) {
		
		// c:\temp 출력
		System.out.println("c:\\temp");
		
		//"hello" 출력
		System.out.println("\"hello\"");
		
		// \t: tab
		// \r : carriage return
		// \n : newline
		// \b: beep (삐- 소리)
		
		// '
		char c = '\'';
		System.out.println(c);
		
		
	}
}
